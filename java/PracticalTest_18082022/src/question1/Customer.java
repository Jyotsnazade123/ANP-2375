package question1;

public class Customer {
	private int coustomer;
	private String customer;
	private int curtomerage;
	
	public Customer() {
		
	}
    
	
	public Customer(int coustomer, String customer, int curtomerage) {
		super();
		this.coustomer = coustomer;
		this.customer = customer;
		this.curtomerage = curtomerage;
	}


	public int getCoustomer() {
		return coustomer;
	}

	public void setCoustomer(int coustomer) {
		this.coustomer = coustomer;
	}

	public String getCustomer() {
		return customer;
	}

	public void setCustomer(String customer) {
		this.customer = customer;
	}

	public int getCurtomerage() {
		return curtomerage;
	}

	public void setCurtomerage(int curtomerage) {
		this.curtomerage = curtomerage;
	}
}
	
	
	
